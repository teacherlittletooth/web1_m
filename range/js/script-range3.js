var zoomImg = document.querySelector("#zoom-img");
var rotate = document.querySelector("#rotate");
var img = document.querySelector("#img");

zoomImg.addEventListener("input", function(){
    console.log(zoomImg.value);
    img.style.width = zoomImg.value + "px";
});

rotate.addEventListener("input", function(){
    console.log(rotate.value);
    img.style.rotate = rotate.value + "deg";
});
